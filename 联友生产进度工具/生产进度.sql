SELECT 
RTRIM(COPTD.TD001) +'-'+ RTRIM(COPTD.TD002) +'-'+RTRIM(COPTD.TD003) AS 订单号, 
CONVERT(INT, COPTD.TD008) AS 订单数量, 
COPTD.TD005 AS 品名, 
COPTD.UDF08 AS 保友品名, 
COPTD.TD006 AS 规格, 
COPTD.UDF10 AS 电商代码, 
COPTD.TD053 AS 配置方案, 
COPTQ.TQ003 AS 配置描述, 
COPTD.TD020 AS 描述备注, 
COPTD.UDF05 AS 客户编码, 
(CASE WHEN TC004='0118' THEN INVMB.UDF04 ELSE INVMB.UDF05 END) AS 生产车间, 
(CASE WHEN COPTC.UDF09='是' THEN 'N' ELSE 'Y' END) AS 急单 

FROM COPTD 
LEFT JOIN COPTC On COPTD.TD001=COPTC.TC001 and COPTD.TD002=COPTC.TC002 
LEFT JOIN COPTQ On COPTD.TD053=COPTQ.TQ002 and COPTD.TD004=COPTQ.TQ001 
LEFT JOIN INVMB ON COPTD.TD004=INVMB.MB001 
WHERE 1=1 AND COPTC.TC027 = 'Y' AND COPTD.TD004 NOT LIKE '6%' AND COPTD.TD004 NOT LIKE '7%' 
AND RTRIM(COPTD.TD001) +'-'+ RTRIM(COPTD.TD002) +'-'+ RTRIM(COPTD.TD003) = '2212-DY001413-0002'